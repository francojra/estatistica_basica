# O que é estatística e para que serve? ----------------------------------------------------------------------------------------------------

### É a parte da matemática que estuda os métodos para coletar, organizar e analisar
### dados de diferentes áreas visando a tomada de decisões.

### "A ciência que se preocupa com a organização, descrição, análise e interpretação
### de dados experimentais visando a tomada de decisões."

### A estatística começou com registro de dados de uma população, como número de habitantes,
### casamentos, moradias, etc. Havia elaboração de tabelas e gráficos que ajudavam a 
### visualizar os podrões.

### A estatística inferencial faz uma inferência a partir do resultado de uma amostra.
### Ela precisa de três pilares: teoria da probabilidade, conceitos de amostragem e
### estatística descritiva.

# População e amostra ----------------------------------------------------------------------------------------------------------------------

### População é o conjuto de elementos que queremos estudar e apresenta alguma característica
### em comum. A amostra é um subconjunto, uma parte da população, que apresenta as mesmas
### características da população.

### A pesquisa censitária é realizada com toda a população. O censo demográfico é realizado
### pelo IBGE. 

### A pesquisa com amostra costuma ser feita para analisar as intenções de votos da população,
### como precisa ser feita regularmente, se torna impossível analisar toda a população, então
### amostras são selecionadas. Uma amostra precisa ter as mesmas características da população,
### para que as pesquisas obtenham resultados mais próximos da população em geral. O processo
### de escolha de amostras é chamado de amostragem.

# Tipos de amostragens ---------------------------------------------------------------------------------------------------------------------

### Um amostra é um pedaço do todo (população). Uma população pode ter várias amostras.
### Dentro das amostras nós temos as principais medidas que são as estatísticas.
### As estatísticas tentam estimar os verdadeiros valores da população, que são os parâmetros.

### Estatísticas são as medidas da amostra e os parâmetros as medidas da população (média, desvio
### padrão, variância, proporções, etc.)

### Representatividade: as amostras devem ter as mesmas características básicas da população no 
### que diz respeito às variáveis que desejamos pesquisar.

### Amostra casual simples: é o método caracterizado por um sorteio aleatório. Os elementos
### da população podem ser enumerados e, em seguida, sorteados entre uma quantidade definida
### previamente.

### Amostra sistemática: é utilizada quando os elementos da população a ser estudada já se encontram
### ordenados e, sem seguida, são selecionados de acordo com uma certa periodicidade.

### Amostragem por conglomerados: pega uma parcela da população, divide ela em pedaços menores
### e amostra os subconjuntos, dentro de cada subconjunto faz a amostragem. Usado em amostragem
### de solo, pega uma amostra, coleta em vários pontos dessa amsotra e faz a homogeneização do solo.

### Amostra proporcional estratificada: consiste em dividir a população em subgrupos (estratos)
### de acordo com outras características da população que devem ser levadas em conta. Um local
### com três características diferentes (três estratos) onde em cada estrato pode-se colocar
### o mesmo número de parcelas.

# Variáveis --------------------------------------------------------------------------------------------------------------------------------

### Quando nos referimos as características da população ou da amostra que nos interessa averiguar
### estatísticamente.

### As variáveis podem assumir valores numéricos ou não numéricos e são classificadas em quantitativas
### ou qualitativas.

### As variáveis quantitativas são medidas em escala numérica, são classificadas em discretas ou 
### contínuas. As variáveis quantitativas discretas podem ser contadas (ex. número de filhos, copos de 
### água ingeridos em um dia). As variáveis quantitativas contínuas representam resultados de medidas
### Exemplo: massa de um indivíduos (kg), tempo gasto em determinada atividade (horas).

### As variáveis qualitativas são características que não possuem valor numérico, são definidas por 
### categorias/classificação. Podem ser classificadas em nominais ou ordinais. As variáveis qualitativas
### nominais não requerem ordenação (ex. cor dos olhos, região onde mora). As variáveis qualitativas 
### ordinais pressupõem uma ordenação. Exemplo: grau de escolaridade, estágio de uma doença, mês de
### observação.

# Organização de dados: tabelas e gráficos -------------------------------------------------------------------------------------------------

### Para organizar os dados obtidos por meio de uma pesquisa, podemos construir tabelas e gráficos.
### O tipo de tabela e gráfico dependem dos tipos de variáveis analisadas.

# Moda -------------------------------------------------------------------------------------------------------------------------------------

### Se refere ao valor que mais aparece em um conjunto de dados.
### Os dados podem ser amodais quando não tem valores repetidos, ou pode ser
### bimidal quando tem mais de um valor repetido.

# Média ------------------------------------------------------------------------------------------------------------------------------------

### A média de um conjunto de dados é a soma de todos os valores dividido pelo
### número total de valores.

### A média é bem sensível a valores muito altas ou muito pequenos (outliers),
### e isso leva a uma distribuição diferente da normal. Nesse caso, a mediana
### pode ser a melhor representação de medida central.

# Mediana ----------------------------------------------------------------------------------------------------------------------------------

### Em um conjunto de dados a mediana é o valor central, que divide o conjunto
### (em ordem crescente ou decrescente) em duas partes iguais, quando não é possível 
### ter apenas um valor central soma os dois calores do centro e divide por 2. 

### Para achar o valor da mediana, é necessário primeiro ordenar os dados.

# Desvio padrão ----------------------------------------------------------------------------------------------------------------------------

### Se refere ao que está mais distante ou menos distante da média. O que "se desvia
### do padrão" que é o valor da média. O menor desvio padrão é o valor que está mais
### próximo da média, e o maior desvio padão é o que está mais distante da média.

# Representações gráficas ------------------------------------------------------------------------------------------------------------------

### Histogramas: representa a frequência de cada intervalo de uma determinada variável.

### A frequência relativa é a razão entre a frequência de um determinado intervalo pela
### soma de todas as frequências.

### A densidade é a frequência relativa dividido pelo tamanho do intervalo. Por exemplo,
### um intervalo entre 80 e 100 apresenta um tamanho de 20.

### Frequência relativa acumulada: acúmulo da frequência de todos os intervalos, porcentagem
### de dados que são acumulados a partir dos primeiros intervalos. O gráfico de frequência
### acumulada apresenta geralmente uma curva exponencial. Portanto, a probabilidade de uma
### frequência de intervalo ser sorteada aumenta com a frequência acumulada.

### Barras: o tamanho da barra representa o valor de cada categoria (característica)
### da variável. No gráfico de pizza ou circular cada barra pode representar porcentagens
### de determinadas variáveis.

### Dispersão: associação entre duas variáveis numéricas (contínuas ou discretas). Pode-se
### verificar a relação entre as duas variáveis, se aumentam concomitantemente, apresentando
### uma relação positiva, ou se relacionam de forma negativa quando uma aumenta a outra 
### variável é reduzida.

### Boxplot: no meio da caixa temos o valor da mediana e abaixo da mediana o 1º quartil,
### e acima da mediana o 2º quartil que representam 50% dos dados.

# Boxplot ----------------------------------------------------------------------------------------------------------------------------------

### Quartis: quando pega o conjunto de dados e divide em quatro partes, o primeiro conjunto
### vai até 25%, o segundo até corresponde a mediana, o terceiro quartil corresponde a 75%
### dos dados e o quarto a porcentagem máxima dos dados. Toda a caixa do boxplot corresponde a 
### 50% dos dados. A mediana corresponde a 25% dos dados acima e 25% dos dados abaixo. Od bigodes
### são as retas acima e abaixo da caixa e todo conjunto representa o diagrama de caixas do boxplot.
### Os outliers são valores extremos que estão fora do diagrama de caixas. 

### Distância Interquatil: representa o valor do terceiro quartil menos o valor do primeiro quartil.
### Ou seja, o tamanho da caixa. O valor de 1,5 vezes a distância interquatil resulta no valor do
### limite inferior ou o limite superior. Quando um valor está acima ou abaixo desses limites, eles
### são considerados outliers. Nem sempre o tamanho do bigode alcança o limite inferior e superior.
### Essa explicação de dados extermos se referem ao critério de outlier de Tuckey.

### Quanto maior a distância interquartil, maior a dispersão dos dados.

# Medidas de dispersão ---------------------------------------------------------------------------------------------------------------------

### Medidas de dispersão são parâmetros utilizados na estatística para calcular o quanto os dados
### de um conjunto de valores podem variar.

### Amplitude: valor máximo do conjunto de dados menos o valor mínimo. A comparação entre amplitudes
### dependerá da distribuição dos dados, dados com outliers pode distorcer as medidas de dispersão.
### Quanto maior a amplitude, maior a dispersão dos dados.

### Desvio médio: define a média e depois pega a distância de cada ponto (valor dos dados) em relação
### a essa média, então pega o valor de todas as distâncias e divide pelo número total de dados.

### Variância: similar ao desvio médio, mas eleva-se a somatória das distâncias ao quadrado. 
### Define a média e depois pega a distância de cada ponto (valor dos dados) em relação
### a essa média, então pega o valor de todas as distâncias e eleva ao quadrado, então divide 
### pelo número total de dados. A variância é útil para determinar o afastamento da média que 
### os dados de um conjunto analisado apresentam. Para isso, determina-se o valor médio das
### diferenças quadradas da média.

### Desvio padrão: é calculado a partir da variância, pois é a raiz quadrada desse parâmetro.

### Coeficiente de variação: pega o valor do desvio padrão, multiplica o valor por 100 e divide
### pela média. O valor é dado em porcentagem.

### Numa distribuição de frequências, quanto mais afunilada a curva normal, menor será a dispersão
### dos dados. Ao redor da média existe maior densidade de valores, ou seja, dados mais constantes
### em torno da média, e menor área abaixo da curva de distribuição.

