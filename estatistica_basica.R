# Moda -------------------------------------------------------------------------------------------------------------------------------------

### Se refere ao valor que mais aparece em um conjunto de dados.
### Os dados podem ser amodais quando não tem valores repetidos, ou pode ser
### bimidal quando tem mais de um valor repetido.

# Média ------------------------------------------------------------------------------------------------------------------------------------

### A média de um conjunto de dados é a soma de todos os valores dividido pelo
### número total de valores.

### A média é bem sensível a valores muito altas ou muito pequenos (outliers),
### e isso leva a uma distribuição diferente da normal. Nesse caso, a mediana
### pode ser a melhor representação de medida central.

# Mediana ----------------------------------------------------------------------------------------------------------------------------------

### Em um conjunto de dados a mediana é o valor central, que divide o conjunto
### (em ordem crescente ou decrescente) em duas partes iguais, quando não é possível 
### ter apenas um valor central soma os dois calores do centro e divide por 2. 

### Para achar o valor da mediana, é necessário primeiro ordenar os dados.

# Desvio padrão ----------------------------------------------------------------------------------------------------------------------------

### Se refere ao que está mais distante ou menos distante da média. O que "se desvia
### do padrão" que é o valor da média. O menor desvio padrão é o valor que está mais
### próximo da média, e o maior desvio padão é o que está mais distante da média.

# Representações gráficas ------------------------------------------------------------------------------------------------------------------

### Histogramas: representa a frequência de cada intervalo de uma determinada variável.

### A frequência relativa é a razão entre a frequência de um determinado intervalo pela
### soma de todas as frequências.

### A densidade é a frequência relativa dividido pelo tamanho do intervalo. Por exemplo,
### um intervalo entre 80 e 100 apresenta um tamanho de 20.

### Frequência relativa acumulada: acúmulo da frequência de todos os intervalos, porcentagem
### de dados que são acumulados a partir dos primeiros intervalos. O gráfico de frequência
### acumulada apresenta geralmente uma curva exponencial. Portanto, a probabilidade de uma
### frequência de intervalo ser sorteada aumenta com a frequência acumulada.

### Barras: o tamanho da barra representa o valor de cada categoria (característica)
### da variável. No gráfico de pizza ou circular cada barra pode representar porcentagens
### de determinadas variáveis.

### Dispersão: associação entre duas variáveis numéricas (contínuas ou discretas). Pode-se
### verificar a relação entre as duas variáveis, se aumentam concomitantemente, apresentando
### uma relação positiva, ou se relacionam de forma negativa quando uma aumenta a outra 
### variável é reduzida.

### Boxplot: no meio da caixa temos o valor da mediana e abaixo da mediana o 1º quartil,
### e acima da mediana o 2º quartil que representam 50% dos dados.

# Boxplot ----------------------------------------------------------------------------------------------------------------------------------

### Quartis: quando pega o conjunto de dados e divide em quatro partes, o primeiro conjunto
### vai até 25%, o segundo até corresponde a mediana, o terceiro quartil corresponde a 75%
### dos dados e o quarto a porcentagem máxima dos dados. Toda a caixa do boxplot corresponde a 
### 50% dos dados. A mediana corresponde a 25% dos dados acima e 25% dos dados abaixo. Od bigodes
### são as retas acima e abaixo da caixa e todo conjunto representa o diagrama de caixas do boxplot.
### Os outliers são valores extremos que estão fora do diagrama de caixas. 

### Distância Interquatil: representa o valor do terceiro quartil menos o valor do primeiro quartil.
### Ou seja, o tamanho da caixa. O valor de 1,5 vezes a distância interquatil resulta no valor do
### limite inferior ou o limite superior. Quando um valor está acima ou abaixo desses limites, eles
### são considerados outliers. Nem sempre o tamanho do bigode alcança o limite inferior e superior.
### Essa explicação de dados extermos se referem ao critério de outlier de Tuckey.

### Quanto maior a distância interquartil, maior a dispersão dos dados.

# Medidas de dispersão ---------------------------------------------------------------------------------------------------------------------

### Medidas de dispersão são parâmetros utilizados na estatística para calcular o quanto os dados
### de um conjunto de valores podem variar.

### Amplitude: valor máximo do conjunto de dados menos o valor mínimo. A comparação entre amplitudes
### dependerá da distribuição dos dados, dados com outliers pode distorcer as medidas de dispersão.
### Quanto maior a amplitude, maior a dispersão dos dados.

### Desvio médio: define a média e depois pega a distância de cada ponto (valor dos dados) em relação
### a essa média, então pega o valor de todas as distâncias e divide pelo número total de dados.

### Variância: similar ao desvio médio, mas eleva-se a somatória das distâncias ao quadrado. 
### Define a média e depois pega a distância de cada ponto (valor dos dados) em relação
### a essa média, então pega o valor de todas as distâncias e eleva ao quadrado, então divide 
### pelo número total de dados. A variância é útil para determinar o afastamento da média que 
### os dados de um conjunto analisado apresentam. Para isso, determina-se o valor médio das
### diferenças quadradas da média.

### Desvio padrão: é calculado a partir da variância, pois é a raiz quadrada desse parâmetro.

### Coeficiente de variação: pega o valor do desvio padrão, multiplica o valor por 100 e divide
### pela média. O valor é dado em porcentagem.

### Numa distribuição de frequências, quanto mais afunilada a curva normal, menor será a dispersão
### dos dados. Ao redor da média existe maior densidade de valores, ou seja, dados mais constantes
### em torno da média, e menor área abaixo da curva de distribuição.

# Estatística descritiva -------------------------------------------------------------------------------------------------------------------

### Corresponde as análises descritivas de dados, como valores de médias e dispersão. É possível
### anvaliar desempenho de jogadores com análises descritivas dos dados. É uma exploração dos dados.

# Parâmetros, estimadores e estimativas ----------------------------------------------------------------------------------------------------

### Parâmetros: representam a média populacional, a variância populacional e a proporção
### amostral. Como não conhecemos esses parâmetros populacionais, utilizamos amostras.

### Estimadores: são as nossas estatísticas, que usamos para estimar os parâmetros da população 
### através das amostras. É uma forma de estimar o verdadeiro parâmetro da população. São
### representados pela média amostral, variância amostral e proporção amostral.

### Estimativas: são os resultados numéricos provenientes dos estimadores das amostras.

### Propriedades das estimativas: justeza e consistência. 

### Justeza: maior justeza ou estimador justo é aquele que o valor esperado do estimador é 
### igual ao parâmetro, ou seja, na média, o estimador se aproxima do parâmetro.

### Consistência: é maior quando se aumenta o n amostral. Quanto maior o n menor a variância
### do estimador. A estimativa se torna mais consistente.

### Estimadores podem ser tendenciosos (com menor justeza e consistência) ou podem ser justos.
### Quanto maior a consistência e justeza, maior a eficácia.

### Estimadores tendenciosos apresentam maior distância da média populacional e também maior
### dispersão em torno da média. Portanto, são menos eficientes.

# Distribuições amostrais ------------------------------------------------------------------------------------------------------------------

### Na distribuição normal temos uma determinada variável aleatória com a maior parte dos valores
### distribuídos em torno da média que se encontra no meio do eixo x. Quanto mais distante da média
### menores são as chances de encontrar os valores na população.

### Função Densidade Probabilidade: é uma função que descreve a verossimilhança de uma variável
### aleatória tomar um valor dado. A probabilidade da variável aleatória cair em uma faixa particular 
### é dada pela integral da densidade dessa variável sobre tal faixa - isto é, é dada pela área 
### abaixo da função densidade mas acima do eixo horizontal e entre o menor e o maior valor dessa 
### faixa.

### A distribuição normal inclui os parâmetros da média e a variância da distribuição normal.

# Intervalo de confiança da média ----------------------------------------------------------------------------------------------------------

### Estimação intervalar: seria a probabilidade de encontrar um determinado valor em relação a
### média populacional. Como muitas vezes não é possível obter esse valor exato da média, define-se
### um intervalo com limites superior e inferior em que exista a probabilidade de encontrar o
### valor da média. Esses limites podem ser traçados com diferentes porcentagens de confiança, como
### 95% por exemplo.

### Quanto mais dados menor a distorção do intervalo de confiança.

### Existe a possibilidade de padronizar um valor do intervalo de confiança, a fórmula é o valor
### do intervalo de confiança menos a média dividido pelo desvio padrão populacional sobre raiz 
### quadrada de N populacional. Isso serve para chegar a uma distribuição normal padronizada.

### O intervalo de confiança é dado pela média +/- o erro (limite superior e inferior das amostras).
### Esse é o intervalo de confiança para a média populacional e não amostral.

### A medida que aumenta o valor do N populacional, por ele estar dividido, o erro é reduzido
### na fórmula. O intervalo é encurtado com o erro menor e fica mais preciso.

# Intervalo de confiança da proporção ------------------------------------------------------------------------------------------------------

### A presenta uma distribuição binomial. Se o número de elementos da amostra multiplicado pelo p
### da proporção for maior que 5, a distribuição se torna discreta e se aproxima da distribuição normal,
### ao invés de binomial. Portanto, nesse intervalo de confiança da proporção a média passa a ser P.

### A variância da proporção será igual a p multiplicado por p-1 dividido por n.

### O intervalo de confiança da proporção será o p (média da proporção) +/- o erro.

# Intervalo de confiança da variância ------------------------------------------------------------------------------------------------------

### A variância amostral é igual a sigma ao quadrado sobre n-1 multiplicado por X² com n-1
### grau de liberdade.

### O intervalo de confiança na curva de distribuição corresponde a 1-alpha (95% de confiança).
### Essa distribuição não é simétrica devido a ser relacionado à variância e não à média.

# Dimensionamento de amostras --------------------------------------------------------------------------------------------------------------

### Corresponde ao número de elementos de amostras que precisa coletar para atingir uma certa
### confiança (1-alpha) e garantir um erro padrão.

### Quantos elementos precisam ser amostrados (n) considerando 95% de confiança, considerando
### um valor fixo do sigma (variância) e um erro aceitável.

### Na fórmula, o valor de n seria o t (ou z) de alpha/2 multiplicado pelo desvio padrão 
### (variância), divididos pelo erro padrão e tudo elevado ao quadrado.

# Teste de hipótese ------------------------------------------------------------------------------------------------------------------------

### H0: hipótese aceita atualmente.
### H1: hipótese alternativa.

### A probabilidade da hipótese ser nula (H0) é quando atinge o valor de p de até 0.05.
### Para a probabilidade da hipótese alternativa ser aceita, ela deve ter o valor de p > 0.05.

### A chance de uma hipótese estar errada é a significância do teste.

### Caso o valor de uma probabilidade ultrapasse o valor de p crítico, então existe apenas
### 5% de chance da hipótese alternativa estar errada. Caso seja menor que o valor crítico,
### então cai dentro da distribuição normal e a hipótese nula é aceita.

### Não pode-se falar que aceita H0 e sim que não rejeita H0. Não se afirma hipótese.

# Teste de hipóteses da média --------------------------------------------------------------------------------------------------------------

### A hipótese nula é que a média da população (M) seja igual a média amostral M0 e a 
### alternativa é que a média seja diferente de M0, ou seja, maior ou menor que M.

### Primeiro passo em um teste de hipótese é definir as hipóteses nula e alternativa, a média,
### e depois o valor crítico.

### Se a hipótese alternativa for que o M seja maior que o M0, então o valor M0 que ultrapassa
### a barreira do valor crítico à direita permite rejeitar a hipótese nula.

### Se a hipótese alternativa for que o M seja maior que o M0, então o valor M0 que ultrpassa
### a barreira do valor crítico à direita permite rejeitar a hipótese nula e aceita a alternativa
### que M é maior que M0. Se não ultrapassar, não rejeita-se a hipótese nula.

### Se a hipótese alternativa for que o M seja menor que o M0, então o valor encontrado que ultrpassa
### a barreira do valor crítico à esquerda permite rejeitar a hipótese nula e aceita a alternativa
### que M é menor que M0. Se não ultrapassar, não rejeita-se a hipótese nula.

# Teste de hipóteses: Erros ----------------------------------------------------------------------------------------------------------------

### Tem relação com a significância do teste (alpha).

### Erro tipo I: rejeita-se a hipótese nula quando ela é verdadeira.

### Erro tipo 2: não rejeita-se a hipótese nula quando ela é falsa. Depende de outra distribuição.

# Teste de hipótese da variância -----------------------------------------------------------------------------------------------------------

### Hipótse nula: duração das chamadas telefônicas com variabilidade de 4 minutos do Brasil é a 
### mesma que uma certa cidade.

### Hipótese alternativa: duração das chamadas telefônicas com variabilidade de 4 minutos do Brasil é  
### diferente (maior ou menor) que uma certa cidade.

### A distribuição da variância é assimétrica (diferente da distribuição normal). Devido à assimetria,
### a cauda da distribuição à direita é 1-alpha/2 e da distribuição à esquerda é alpha/2.

### Quando se testa a hipótese alternativa que diz apenas que é diferente, sem ser maior ou menor,
### deve-se achar dois valores críticos e verificar se ultrapassam as barras desses valores 
### críticos, se ultrapassar, então aceita-se a hipótese alternativa.

# Teste de Hipóteses de Duas Médias --------------------------------------------------------------------------------------------------------

### A média amostral apresenta um valor esperado que é a média populacional e a variância populacional
### dividido pelo número de elementos da amostra.

### A hipótese nula desse teste é que as médias dos dois grupos são iguais e a hipótese alternativa
### é que a média dos dois grupos são diferentes (maior ou menor).

### Quando as variâncias são iguais usa-se a distribuição de t-student.

### Da mesma forma que os testes anteriores, deve-se ecnontrar usando a fórmula o valor crítico.

# Teste de Hipóteses de Médias Pareadas ----------------------------------------------------------------------------------------------------

### Ocorre quando analisa duas populações ao mesmo tempo em relação a uma determinada variável.
### Por exemplo, o tempo que dois alunos passam para responder uma determinada questão. A medida
### que esses mesmos alunos respondem as próximas questões, eles vão formando pares. O tempo de
### resposta de cada questão depende da própria questão e do candidato que responde.

### No caso dos valores serem pareados, as médias são dependentes.

### Com os dados de tempo de cada variável, pode-se calcular a variável de diferença de tempos
### dos dois candidatos. Esse cálculo é feito para os dois tempos de cada questão, então teremos
### um conjunto de valores com as diferenças de tempos que será a amostra.

### A hipótese nula é que a média dessa amostra será igual a zero, e a hipótese alternativa
### é que a média será maior ou menor que zero.

### Deve-se achar na fórmula o valor crítico da distribuição e a média da amostra para saber se
### essa média ultrapassa ou não a barreira do valor crítico.

# Teste de hipóteses de duas variâncias ----------------------------------------------------------------------------------------------------

### Também conhecido como teste F.

### Na hipótese nula a variância de um grupo A é igual a variância de um grupo B. A hipótese alternativa
### ocorre quando um grupo A é diferente (maior ou menor) de um grupo B.

### Hipótese nula: variância de A dividido pela variância de B é igual a 1. Ou seja, variâncias iguais.

### Hipótese alternativa: variância de A dividido pela variância de B é diferente de 1. Ou seja,
### variância diferentes.

### A Estatística de Teste é a variância amostral do grupo A dividido pela variância amostral do
### grupo B. O nome dessa estatística é: estatística F de Fisher e Snedecor. Essa estatística
### também tem uma distribuição de probabilidade e se chama distribuição F de Snedecor.

### A distribuição de probabilidade é assimétrica. O pico da distribuição é igual a 1.

### A distribuição apresenta um F crítico superior e um F crítico inferior. A partir desses dois
### valores deve-se buscar um valor amostral (F = varA/varB) para verificar se ele ultrapassa 
### ou não os valores do F crítico.

### Para encontrar os valores de F crítico na tabela, deve-se ter os valores da significância e os
### valores de n amostral.

# Análise de Variância - ANOVA -------------------------------------------------------------------------------------------------------------

### Hipótese nula: k médias apresentam médias iguais.

### Hipótese alternativa: ao menos a média de um dos grupos é diferente das demais.

### Utiliza-se o teste F: variância entre amostras dividido pela variância do resíduo.

### Soma entre amostras: calculada como a soma dos quadrados entre amostras dividido
### pelos graus de liberdade entre amostras.

### Soma total: soma dos quadrados totais dividido pelos graus de liberdade totais.

### Soma residual: soma dos quadrados da variância (residuais) dividido pelos graus de 
### liberdade residuais k(n-1).

### A soma de quadrados totais se refere a soma dos quadrados de todas as variáveis (grupos)
### analisadas, ou seja, a soma de todos os valores das amostras menos a média dos grupos elevados ao quadrado, 
### dividido pelos graus de liberdade que é nk - 1 (k são os grupos e n o número de observações).

### A soma dos quadrados entre amostras é o n (número de amostras) vezes a soma dos quadrados
### dividido pelos graus de liberdade que é k - 1, onde k é o número de médias (grupos).

### A soma de quadrados residuais pode ser o valor da soma de quadrados totais menos o valor
### da soma de quadrados entre amostras.

# ANOVA com Dois Fatores -------------------------------------------------------------------------------------------------------------------

### Na ANOVA com dois fatores é acrescentado mais um teste de hipótese. Nesse caso, utiliza-se
### a média entre as colunas e entre linhas.

### Hipótese nula: média igual em todas as linhas.

### Hipótese alternativa: a média de pelo menos uma linha é diferente.

### Hipótese nula: média igual em todas as colunas.

### Hipótese alternativa: a média de pelo menos uma coluna é diferente.

### Pode ocorrer diferença para apenas um dos fatores testados, para os dois fatores,
### ou para nenhum deles. Serão calculados dois valores de F críticos e dois valores
### de F calculados.

### Se o F crítico for maior que o F calculado não rejita-se a hipótese nula. Se o F crítico
### for menor que o F calculado rejeita-se a hipótese nula.

### Após o teste F: na hipótese nula a variância entre linhas é igual a variância dos resíduos.
### E na hipótese alternativa, a variância entre linhas é maior que a variância dos resíduos (pelo
### menos uma das populações diferente das demais em média).

# ANOVA com Repetições ---------------------------------------------------------------------------------------------------------------------

### Analisa a diferença entre médias ao longo do tempo, por exemplo, desemenho de jogadores ao
### longo de diferentes temporadas de jogos.

### Ocorrem repetições dentro das células das linhas e das colunas.

### Hipótese nula: média iguais em todas as linhas ou colunas. 

### Hipótese alternativa: a média de pelo menos uma linha ou coluna é diferente. 

### Para essa análise são calculados quatro valores de F críticos e quatro valores de F calculados.

### Primeiro, deve-se verificar o valor do F crítico e calculado da interação para saber se existe ou não
### interações. Caso não exista interações (H0), calcula-se o F crítico e calculado das amostras entre linhas, 
### entre colunas e entre tratamentos (repetições) usando-se a soma dos quadrados dos resíduos
### para o cálculo desses F críticos. Caso exista interações (H1), utiliza-se o valor da soma dos
### quadrados das interações.

# Correlação e Regressão Linear ------------------------------------------------------------------------------------------------------------

### Coeficiente de correlação: medida de associação entre duas variáveis x e y.

### Coeficiente de correlação amostral é chamado de r e mede a intensidade de
### correlação entre as duas variáveis aleatórias x e y.

### Os valores de x e y correspondentes são chamados de pares ordenados.

### Modelo de Regressão Linear Simples (MRLS).

### O x é uma variável independente e os valores de y variam em torno dessa variável.

### Exemplos: altura de pai vs altura de filho, peso vs altura.

### Correlação linear perfeita: r = 1. As correlações fracas apresentam o r próximo
