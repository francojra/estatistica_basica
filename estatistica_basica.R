# O que é estatística e para que serve? ----------------------------------------------------------------------------------------------------

### É a parte da matemática que estuda os métodos para coletar, organizar e analisar
### dados de diferentes áreas visando a tomada de decisões.

# População e amostra ----------------------------------------------------------------------------------------------------------------------

### População é o conjuto de elementos que queremos estudar e apresenta alguma característica
### em comum. A amostra é um subconjunto, uma parte da população, que apresenta as mesmas
### características da população.

### A pesquisa censitária é realizada com toda a população. O censo demográfico é realizado
### pelo IBGE. 

### A pesquisa com amostra costuma ser feita para analisar as intenções de votos da população,
### como precisa ser feita regularmente, se torna impossível analisar toda a população, então
### amostras são selecionadas. Uma amostra precisa ter as mesmas características da população,
### para que as pesquisas obtenham resultados mais próximos da população em geral. O processo
### de escolha de amostras é chamado de amostragem.

# Tipos de amostragens ---------------------------------------------------------------------------------------------------------------------

### Amostra casual simples: é o método caracterizado por um sorteio aleatório. Os elementos
### da população podem ser enumerados e, em seguida, sorteados entre uma quantidade definida
### previamente.

### Amostra sistemática: é utilizada quando os elementos da população a ser estudada já se encontram
### ordenados e, sem seguida, são selecionados de acordo com uma certa periodicidade.

### Amostra proporcional estratificada: consiste em dividir a população em subgrupos (estratos)
### de acordo com outras caracter´siticas da população que devem ser levadas em conta.

# Variáveis --------------------------------------------------------------------------------------------------------------------------------

### Quando nos referimos as características da população ou da amostra que nos interessa averiguar
### estatísticamente.

### As variáveis podem assumir valores numéricos ou não numéricos e são classificadas em quantitativas
### ou qualitativas.

### As variáveis quantitativas são medidas em escala numérica, são classificadas em discretas ou 
### contínuas. As variáveis quantitativas discretas podem ser contadas (ex. número de filhos, copos de 
### água ingeridos em um dia). As variáveis quantitativas contínuas representam resultados de medidas
### Exemplo: massa de um indivíduos (kg), tempo gasto em determinada atividade (horas).

### As variáveis qualitativas são características que não possuem valor numérico, são definidas por 
### categorias/classificação. Podem ser classificadas em nominais ou ordinais. As variáveis qualitativas
### nominais não requerem ordenação (ex. cor dos olhos, região onde mora). As variáveis qualitativas 
### ordinais pressupõem uma ordenação. Exemplo: grau de escolaridade, estágio de uma doença, mês de
### observação.

# Organização de dados: tabelas e gráficos -------------------------------------------------------------------------------------------------

### Para organizar os dados obtidos por meio de uma pesquisa, podemos construir tabelas e gráficos.
### O tipo de tabela e gráfico dependem dos tipos de variáveis analisadas.

# Moda -------------------------------------------------------------------------------------------------------------------------------------

### Se refere ao valor que mais aparece em um conjunto de dados.
### Os dados podem ser amodais quando não tem valores repetidos, ou pode ser
### bimidal quando tem mais de um valor repetido.

# Média ------------------------------------------------------------------------------------------------------------------------------------

### A média de um conjunto de dados é a soma de todos os valores dividido pelo
### número total de valores.

# Mediana ----------------------------------------------------------------------------------------------------------------------------------

### Em um conjunto de dados a mediana é o valor central, que divide o conjunto
### (em ordem crescente ou decrescente) em duas partes iguais, quando não é possível 
### ter apenas um valor central soma os dois calores do centro e divide por 2. 

# Desvio padrão ----------------------------------------------------------------------------------------------------------------------------

### Se refere ao que está mais distante ou menos distante da média. O que "se desvia
### do padrão" que é o valor da média. O menor desvio padrão é o valor que está mais
### próximo da média, e o maior desvio padão é o que está mais distante da média.
